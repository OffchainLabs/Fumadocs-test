/**
 * generate-partials-catalog — build the writer-facing catalog of reusable partials.
 *
 * Emits two always-fresh artifacts under `content/partials/`:
 *   - CATALOG.md    — human index: every partial grouped by area, with title, summary, tags,
 *                     usage count, and a copy-paste `<include cwd>` snippet. Search by intent.
 *   - manifest.json — machine index for agents/tooling (same data, structured).
 *
 * Usage:
 *   node scripts/generate-partials-catalog.mjs            # write both files
 *   node scripts/generate-partials-catalog.mjs --check    # exit 1 if either file is stale (CI)
 */

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import path from 'node:path';
import { buildCatalog, CATALOG_FILE, MANIFEST_FILE } from './lib/partials.mjs';

const repoRoot = process.cwd();

/** Top-level area a partial belongs to (first path segment under content/partials, else "general"). */
function area(relPath) {
  const segs = relPath.replace(/^content\/partials\//, '').split('/');
  return segs.length > 1 ? segs[0] : 'general';
}

function renderCatalog(records) {
  const byArea = new Map();
  for (const r of records) {
    const a = area(r.path);
    if (!byArea.has(a)) byArea.set(a, []);
    byArea.get(a).push(r);
  }

  const lines = [
    '<!-- GENERATED by scripts/generate-partials-catalog.mjs — do not edit by hand. -->',
    '',
    '# Partials catalog',
    '',
    'Reusable content fragments, inlined into pages with `<include cwd>…</include>`. **Before writing a',
    'banner, note, config table, or troubleshooting block, search this file** (⌘F by intent) and reuse',
    'the partial instead of duplicating prose. Curate titles, summaries, and tags in',
    '`content/partials/registry.json`.',
    '',
    `${records.length} partials.`,
    '',
  ];

  for (const a of [...byArea.keys()].sort()) {
    lines.push(`## ${a}`, '');
    for (const r of byArea.get(a).sort((x, y) => x.path.localeCompare(y.path))) {
      const tags = r.tags.length ? ` — tags: ${r.tags.map((t) => `\`${t}\``).join(', ')}` : '';
      const used = r.usedIn === 0 ? '⚠️ unused' : `used in ${r.usedIn} page${r.usedIn === 1 ? '' : 's'}`;
      lines.push(`### ${r.title}`, '');
      if (r.summary) lines.push(r.summary, '');
      lines.push(`- \`${r.snippet}\``, `- ${used}${tags}`, '');
    }
  }
  return lines.join('\n');
}

function renderManifest(records) {
  return JSON.stringify(records, null, 2) + '\n';
}

function main() {
  const records = buildCatalog(repoRoot);
  const catalog = renderCatalog(records);
  const manifest = renderManifest(records);
  const catalogAbs = path.join(repoRoot, CATALOG_FILE);
  const manifestAbs = path.join(repoRoot, MANIFEST_FILE);

  if (process.argv.slice(2).includes('--check')) {
    const stale = [];
    if (!existsSync(catalogAbs) || readFileSync(catalogAbs, 'utf8') !== catalog) stale.push(CATALOG_FILE);
    if (!existsSync(manifestAbs) || readFileSync(manifestAbs, 'utf8') !== manifest) stale.push(MANIFEST_FILE);
    if (stale.length) {
      console.error(`partials catalog stale: ${stale.join(', ')}. Run \`pnpm partials:catalog\`.`);
      process.exit(1);
    }
    console.log('partials catalog: up to date.');
    return;
  }

  writeFileSync(catalogAbs, catalog);
  writeFileSync(manifestAbs, manifest);
  console.log(`partials catalog: wrote ${records.length} entries to ${CATALOG_FILE} + ${MANIFEST_FILE}.`);
}

main();
